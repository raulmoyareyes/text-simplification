    private void miOpenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_miOpenActionPerformed

        String os = System.getProperty("os.name");
        File file;
        String zenity = "zenity --file-selection --title=Open";
        String filestring = null;
        if ((os.contains("nix") || os.contains("nux"))) {
            //Use native Linux file selection.
            try {
                Process p = Runtime.getRuntime().exec(zenity);
                BufferedReader br = new BufferedReader(new InputStreamReader(p.getInputStream()));
                StringBuilder sb = new StringBuilder();
                String line;
                sb.append(br.readLine());
                filestring = sb.toString();
            } catch (IOException e1) {
                System.out.println("Problem in file");
            }
            file = new File(filestring);
            try {
                txtaOriginal.read(new FileReader(file.getAbsolutePath()), null);
            } catch (IOException ex) {
                System.out.println("Problem accessing file" + file.getAbsolutePath());
            }
        } else {
            int returnVal = jFileChooser1.showOpenDialog(jFrame1);
            if (returnVal == JFileChooser.APPROVE_OPTION) {
                file = jFileChooser1.getSelectedFile();
                try {
                    // What to do with the file, e.g. display it in a TextArea
                    txtaOriginal.read(new FileReader(file.getAbsolutePath()), null);
                } catch (IOException ex) {
                    System.out.println("Problem accessing file" + file.getAbsolutePath());
                }
            } else {
                System.out.println("File access cancelled by user.");
            }
        }

    }//GEN-LAST:event_miOpenActionPerformed

