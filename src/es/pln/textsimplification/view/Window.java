    private void btnURLActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnURLActionPerformed
        String page = "";
        String iUrl = JOptionPane.showInputDialog(null, "Select a url", "Url", JOptionPane.NO_OPTION);
        try {
            URL url = new URL(iUrl);
            URLConnection yc = url.openConnection();
            try (BufferedReader in = new BufferedReader(new InputStreamReader(yc.getInputStream(),"UTF-8"))) {
                String inputLine;
                while ((inputLine = in.readLine()) != null) {
                    page += inputLine;
                }
                in.close();
            }
        } catch (IOException e) {
            //
        }
        txtaOriginal.setText(Jsoup.parse(page).text());
    }//GEN-LAST:event_btnURLActionPerformed

