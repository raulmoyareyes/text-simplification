    private void miSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_miSaveActionPerformed

        String os = System.getProperty("os.name");
        File file;
        String zenity = "zenity --file-selection --title=Save --save --confirm-overwrite";
        String filestring = null;
        if ((os.contains("nix") || os.contains("nux"))) {
            //Use native Linux file selection.
            try {
                Process p = Runtime.getRuntime().exec(zenity);
                BufferedReader br = new BufferedReader(new InputStreamReader(p.getInputStream()));
                StringBuilder sb = new StringBuilder();
                String line;
                sb.append(br.readLine());
                filestring = sb.toString();
            } catch (IOException e1) {
                System.out.println("Problem in file");
            }
            file = new File(filestring);
            String sb = txtaSimplified.getText();
            try (FileWriter fw = new FileWriter(filestring)) {
                fw.write(sb);
            } catch (IOException ex) {
                System.out.println("Problem to save " + filestring);
            }
        } else {
            String sb = txtaSimplified.getText();
            JFileChooser chooser = new JFileChooser();
            chooser.setCurrentDirectory(new File(""));
            int retrival = chooser.showSaveDialog(null);
            if (retrival == JFileChooser.APPROVE_OPTION) {
                try (FileWriter fw = new FileWriter(chooser.getSelectedFile())) {
                    fw.write(sb);
                } catch (IOException ex) {
                    System.out.println("Problem to save " + chooser.getSelectedFile());
                }
            }
        }

    }//GEN-LAST:event_miSaveActionPerformed

